package loop;

public class Do_While {

	public static void main(String[] args) {
		// forward number
		int l=1;
		do {
			System.out.print(l+" ");
			l++;
		}while(l<=40);
		
		   System.out.println(" ");
		   System.out.println("--------------------------------------");
		   System.out.println(" ");
		   
		   // Reverse number
		   int y=1;
			do {
				System.out.print(y+" ");
				y--;
			}while(y<=40);
		   
		

	}

}
