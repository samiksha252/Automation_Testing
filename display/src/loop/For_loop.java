package loop;

public class For_loop {

	public static void main(String[] args) {
		// 1 to 10
		for(int a=1; a<11; a++ ) {
			System.out.println(a);
		}
		System.out.println("-----------------------------------");
		// 10 to 1
		
		for(int b=10; b>=1; b-- ) {
			System.out.println(b);
		}
		System.out.println("----------------------------");
		
		for(double c=1.1;c<=10.1;c++) {
			System.out.println(c);
		}
	    System.out.println("--------------------------------");
	    // 50 to 100 
	    for(int d=50; d<101; d++ ) {
			System.out.println(d);
		}
	    System.out.println("--------------------------------");
	    // concatination
	    
	     for(int e=1; e<=20; e++) {
	    	 System.out.println( + e);
	     }
	     System.out.println("--------------------------------");
	     
	     //Even number(divisible by 2)
	     for( int f=1;f<=10;f++) {
	    	 if(f%2==0){
	    		 System.out.println(f);
	    	 }
	     }
	     
	     System.out.println("--------------------------------");
	    // 100 to 1
	      for(int g=100;g>=1;g--) {
	    	  if(g%2==0) {
	    		  System.out.println(g);
	    	  }
	      }
	      System.out.println("-------------------------------");
	     
	      // Odd Number 1 to 100 (Not divisible by 2)
	      for(int h=1;h<=100;h++) {
	    	  if(h%2!=0) {
	    		  System.out.println(h);
	    	  }
	      }
	      System.out.println("---------------------------------");
	      
	      // Odd Number(100 to 1)
	      for(int j=100;j>1;j--) {
	    	  if(j%2!=0) {
	    		  System.out.println(j);
	    	  }
	      }
	      System.out.println("----------------------------------");
	       
	      //   to print the table 5
	      
	        for( int l = 1;l<=10;l++) {
	    	 System.out.println(5*l);
	        }
	        System.out.println("----------------------------");
	        
	        // Even  Number without using if statement
		      for( int m = 1;m<=100;m++) {
			    	 System.out.println(2*m);
			        }
		      System.out.println("---------------------------");
		      
		      //6*1=6
		      //6*2=12
		      
		      for( int n=1;n<=10;n++) {
			    	 System.out.println("6*"+n+"="+(6*n));
			        }
		      System.out.println("-----------------------------");
		      
		 
		      
	}
}

	      
	      
	      
	    	 
	     
	     
	     
	   
	    
	    
	    
		
		
		
		
		
		
		

	


