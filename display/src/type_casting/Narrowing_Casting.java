package type_casting;

public class Narrowing_Casting {

	public static void main(String[] args) {
		 long t=200;
		 int b= (int) t;
		 System.out.println(b);
		 
		 System.out.println("--------------------------------------");
		 
		  double r=56.89f;
		  float w=(float) r;
		 System.out.println(w);

	}

}
