package control_statement;

       public class If_statement {

       public static void main(String[] args) {
              //initialize variable
		int p =90;
		 
		if(p>100) {
			System.out.println("p is greater than 100");
		}

		if (p<100) {
			System.out.println("p is less than 100");
	      }
		System.out.println("----------------------------------");
		
		//Logical and(&&)
		 if(p==90 && p<100) {
			 System.out.println("execute the above code");
		 }
		 System.out.println("----------------------------------");
		 
		 //Logical or(||)
		if (p==80 || p>100){
		 System.out.println("execute above code");
		 }
	     }
       }
       
    

      
		 
    