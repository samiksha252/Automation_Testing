package sam_02;

public class Comparision_operator {

	public static void main(String[] args) {
		//Greater than
		 int  l=90;
		int p= 40;
		boolean k=l>p;
		System.out.println(k);
		System.out.println("--------------------------------------");
		//Less Than
		short z =80;
		long d = 76;
		boolean y=z<d;
		System.out.println(y);
		System.out.println("--------------------------------------");
		//Greater than equal to
		int r=80;
		int j=80;
		boolean w = r>=j;
		System.out.println(w);
		System.out.println("--------------------------------------");
		//Less Than equal to
		int b=34;
		short u=90;
		boolean x =b<u;
		System.out.println(x);
		System.out.println("--------------------------------------");
		//equals to 
		String e="Samiksha";
		String h="Samiksha";
		System.out.println(e==h);
		System.out.println("--------------------------------------");
		//Not Equals to
		char v='o';
		char n='k';
		System.out.println(v!=n);

	}

}
