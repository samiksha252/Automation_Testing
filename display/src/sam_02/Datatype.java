package sam_02;

public class Datatype {

	public static void main(String[] args) {
		// Numeric Data Type 
		//Integer Data type
	      byte a=80;
	      System.out.println( "The value of byte a  is: "+ a);
	      System.out.println("-------------------------------");
	      short b=70;
	      System.out.println("The value of short b is: "+ b);
	      System.out.println("-------------------------------");
	      int r=45;
	      System.out.println("The value of int  r is: "+ r);
	      System.out.println("-------------------------------");
	      long s=60;
	      System.out.println("The value of long s is: "+ s);
	      System.out.println("-------------------------------");
	      
	      //Floating Data type
	      float h=89.790f;
	      System.out.println("The value float h is: "+ h);
	      System.out.println("-------------------------------");
	      double t=7878.98765;
	      System.out.println("The value of double t  is: "+ t);
	      System.out.println("-------------------------------");
	      
	      //Non Numeric Data Type
	      //Boolean data type
	          int u= 70;
	          boolean j= u<90;
	          System.out.println("The value of boolean j  is: "+ j);
	          System.out.println("-------------------------------");
	          
	          char k='m';
	          System.out.println("The value of char k is: "+ k);
	          System.out.println("-------------------------------");
	          
	          //Non Primitive Data type
	           String o="samiksha aswale";
	           System.out.println("The value of String o is: "+ o);
		
		

	}

}
