package array;

public class Single_array1 {

	public static void main(String[] args) {
		// Without allocating memory
		
	float[ ] percent= { 77.89f,45.89f,89.90f};
	
	
	    // for each loop
	for(float p: percent) {
		System.out.println(p);
		System.out.println("----------------------------");
		
		// for loop
		for(int a=0;a<3;a++) {
			System.out.println(percent[a]);
			
			// string 
		String[] name= { "samiksha","Aswale"};
		for(String u: name) {
			System.out.println(u);
			
		}
		}
	}
	}
}


